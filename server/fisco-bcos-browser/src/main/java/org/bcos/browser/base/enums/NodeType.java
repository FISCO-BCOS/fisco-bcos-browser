package org.bcos.browser.base.enums;

/**
 * Enumeration of node type.
 */
public enum NodeType {
    CONSENSUS(0), OBSERVER(1); // 0-consensus;1-observer

    private int value;

    private NodeType(Integer dataStatus) {
        this.value = dataStatus;
    }

    public int getValue() {
        return this.value;
    }
}
